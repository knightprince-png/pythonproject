# Arithmetic operators
x=50
y=32
print(x+y)
print(x-y)
print(x*y)
print(x/y)
print(x%y) # modulus - returns remainder after division

#comparison operators
print(x<y)
print(x>y)
print(x>=y)
print(x<=y)
print(x==y)
print(x!=y)

#assignment operators
a=10

a +=2

print(a)

#logical operators -and, or,not
print(34<50 and 56>200)
print(34 < 50 or 56 > 200)
print(not(34<50 or 56>200))